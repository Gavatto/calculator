fun main() {
    // Заданий склад горючої маси мазуту за варіантом 4
    val H_G = 3.4
    val C_G = 70.6
    val S_G = 2.7
    val O_G = 1.9
    val Qi_daf = 40.40 // Нижча теплота згоряння горючої маси мазуту, МДж/кг
    val W_R = 5.0 // Вологість робочої маси палива, %
    val A_D = 15.2 // Зольність сухої маси, %

    // Перерахунок елементарного складу на робочу масу
    val C_R = C_G * (100 - W_R - A_D) / 100
    val H_R = H_G * (100 - W_R - A_D) / 100
    val S_R = S_G * (100 - W_R - A_D) / 100
    val O_R = O_G * (100 - W_R - A_D) / 100
    val A_R = A_D * (100 - W_R) / 100

    println("Склад робочої маси мазуту:")
    println("C_R: $C_R%")
    println("H_R: $H_R%")
    println("S_R: $S_R%")
    println("O_R: $O_R%")
    println("A_R: $A_R%")

    // Розрахунок нижчої теплоти згоряння мазуту на робочу масу
    val Qi_r = Qi_daf * (100 - W_R - A_D) / 100
    println("Нижча теплота згоряння мазуту на робочу масу: $Qi_r МДж/кг")
}
